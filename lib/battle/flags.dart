/// Returns a random flag emoji from [kWorldFlags].
String randomFlag({math.Random? random, String? exclude}) {
  final rng = random ?? math.Random();
  if (kWorldFlags.isEmpty) {
    return 'ðŸ³ï¸';
  }
  if (exclude == null || !kWorldFlags.contains(exclude)) {
    return kWorldFlags[rng.nextInt(kWorldFlags.length)];
  }
  final available = kWorldFlags.where((flag) => flag != exclude).toList();
  if (available.isEmpty) {
    return exclude;
  }
  return available[rng.nextInt(available.length)];
}
